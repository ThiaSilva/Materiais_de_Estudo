git init # inicia o git no repositorio
git clone <URL> # Copiar repositorio remoto 
git status # Lista as modificações atyaus do repositorio
git log # Lista historico de commits 
git add <nome-arquivo> # Adiciona o arquivo do palco (stage)
git reset <nom-arquivo> # Remove arquivo do palco (stage)
git commit -m "mensagem" # Realiza o commit das modificações 
git branch # Lista todas as branch locais
git branch <nome-branch> #cria uma nova branch
git checkout <nome-branch> # entra na branch
git pull original <nome-branch> # Puxa as modificações 
git push origin <nome-branch> # Envia as modificações 